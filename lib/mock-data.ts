export function formatSalary(amount: number, currency: string): string {
  const currencyInfo = CURRENCIES.find((c) => c.code === currency)
  const symbol = currencyInfo?.symbol || currency
  if (amount >= 1000000) {
    return `${symbol} ${(amount / 1000000).toFixed(1)}M`
  }
  if (amount >= 1000) {
    return `${symbol} ${(amount / 1000).toFixed(0)}K`
  }
  return `${symbol} ${amount.toLocaleString()}`
}

export function getNetScore(entry: SalaryEntry): number {
  return entry.upvotes - entry.downvotes
}

export function isApproved(entry: SalaryEntry): boolean {
  return getNetScore(entry) >= VERIFICATION_THRESHOLD
}
